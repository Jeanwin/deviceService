package com.vo;
/**
 * rtmp流信息
 * @author gly
 *
 */
public class RtmpInfo {
	private String rtmp_repeater;
	private String uid;
	private String rtmp_filename;
	public String getRtmp_repeater() {
		return rtmp_repeater;
	}
	public void setRtmp_repeater(String rtmp_repeater) {
		this.rtmp_repeater = rtmp_repeater;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getRtmp_filename() {
		return rtmp_filename;
	}
	public void setRtmp_filename(String rtmp_filename) {
		this.rtmp_filename = rtmp_filename;
	}
	
}
